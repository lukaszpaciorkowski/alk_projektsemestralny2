st.divider()
st.header("Analytics Methods Reference")

all_fns = list(REGISTRY.values())
n_total = len(all_fns)
n_generic = sum(1 for f in all_fns if f.scope == "generic")
n_specialized = n_total - n_generic

st.markdown(
    f"This application provides **{n_total} analytical methods**. "
    f"**{n_generic} generic methods** work on any imported dataset. "
    f"**{n_specialized} specialised methods** are available when a matching dataset type is detected "
    "(e.g. diabetes-specific analyses appear only when a diabetes dataset is loaded)."
)

# ── Generic methods ──────────────────────────────────────────────────────────
st.subheader("🌐 Generic Methods")
st.caption("Work on any dataset regardless of type.")

generic_fns = [f for f in all_fns if f.scope == "generic"]
for fn in generic_fns:
    with st.expander(f"**{fn.label}**", expanded=False):
        st.markdown(fn.description)

        _render_method_detail(fn.id)

        if fn.params:
            rows = []
            for p in fn.params:
                widget_label = _WIDGET_LABELS.get(p.widget, p.widget)
                options_str = ", ".join(str(o) for o in p.options) if p.options else "—"
                rows.append({
                    "Parameter": p.label or p.name,
                    "Type": widget_label,
                    "Default": str(p.default),
                    "Options": options_str,
                })
            import pandas as pd
            st.table(pd.DataFrame(rows))
        else:
            st.caption("No parameters — runs automatically on selection.")

        chart_info = f"Chart: **{fn.chart_type}**" if fn.chart_type else "Chart: none"
        st.caption(chart_info)

# ── Specialised methods ───────────────────────────────────────────────────────
