def _get_engine():
    try:
        return get_engine(DB_PATH)
    except Exception:
        return None


def _render_png(mermaid_src: str, mmdc: str) -> bytes | None:
    with tempfile.TemporaryDirectory() as tmp:
        mmd_path = Path(tmp) / "diagram.mmd"
        png_path = Path(tmp) / "diagram.png"
        mmd_path.write_text(mermaid_src, encoding="utf-8")
        result = subprocess.run(
            [mmdc, "-i", str(mmd_path), "-o", str(png_path),
             "-w", "2400", "-H", "1800", "--scale", "2", "-b", "transparent"],
            capture_output=True,
            text=True,
            timeout=120,
        )
        if result.returncode == 0 and png_path.exists():
            return png_path.read_bytes()
    return None


def _save_to_docs(name: str, mermaid_src: str, png_bytes: bytes | None) -> None:
    DIAGRAMS_DIR.mkdir(parents=True, exist_ok=True)
    _MMD_FILES[name].write_text(mermaid_src, encoding="utf-8")
    if png_bytes is not None:
        _PNG_FILES[name].write_bytes(png_bytes)


def _render_method_detail(fn_id: str) -> None:
    """Render the How it works / Expected data / Libraries subsections."""
    detail = _METHOD_DETAIL.get(fn_id)
    if not detail:
        return

    st.markdown("**How it works**")
    st.markdown(detail["how_it_works"])

    st.markdown("**What data it needs**")
    ed = detail["expected_data"]
    st.markdown(
        f"- **Column types:** {ed['column_types']}\n"
        f"- **Minimum rows:** {ed['min_rows']}\n"
        f"- **Notes:** {ed['notes']}"
    )

    st.markdown("**Python libraries used**")
    for lib in detail["libraries"]:
        st.markdown(f"- {lib}")


