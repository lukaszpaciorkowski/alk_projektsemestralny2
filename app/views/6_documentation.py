# Section 1: Architecture Diagrams
# ===========================================================================

st.header("Architecture Diagrams")
st.markdown(
    "Diagrams are saved in `docs/diagrams/`. "
    "Click **Regenerate** to rebuild from the live database and codebase."
)

mmdc_bin = shutil.which("mmdc")

if st.button("🔄 Regenerate Diagrams", help="Rebuild all diagrams from the live DB and codebase"):
    if engine is None:
        st.error("Database not available. Import a dataset on the **Data Sources** page first.")
    else:
        saved_mmd = 0
        rendered_png = 0
        errors: list[str] = []

        with st.spinner("Generating and rendering diagrams…"):
            generators = {
                "ER Diagram":       lambda: generate_er_mermaid(engine),
                "Pipeline Flow":    lambda: generate_pipeline_mermaid(engine),
                "App Architecture": generate_app_architecture_mermaid,
            }
            for name, gen_fn in generators.items():
                try:
                    src = gen_fn()
                except Exception as exc:
                    errors.append(f"{name}: generation failed — {exc}")
                    continue

                png_bytes_regen: bytes | None = None
                if mmdc_bin:
                    png_bytes_regen = _render_png(src, mmdc_bin)
                    if png_bytes_regen:
                        rendered_png += 1
                    else:
                        errors.append(f"{name}: mmdc failed to render PNG")

                _save_to_docs(name, src, png_bytes_regen)
                saved_mmd += 1

        parts = [f"Generated {saved_mmd} diagram(s)"]
        if rendered_png:
            parts.append(f"rendered {rendered_png} PNG(s)")
        parts.append("saved to docs/diagrams/")
        st.success(" · ".join(parts))
        if not mmdc_bin:
            st.info("Install Mermaid CLI for PNG rendering: `npm install -g @mermaid-js/mermaid-cli`")
        for err in errors:
            st.warning(err)
        st.rerun()

selected = st.radio("Select diagram", options=_DIAGRAM_KEYS, horizontal=True)

png_path = _PNG_FILES[selected]
mmd_path = _MMD_FILES[selected]

st.divider()

if png_path.exists():
    png_bytes = png_path.read_bytes()
    st.image(png_bytes, caption=selected, use_container_width=True)
    st.download_button(
        label=f"Download {selected} PNG",
        data=png_bytes,
        file_name=png_path.name,
        mime="image/png",
    )
elif mmd_path.exists():
    st.info("PNG not rendered — showing Mermaid source. Install `mmdc` and click Regenerate for PNG.")
    st.code(mmd_path.read_text(encoding="utf-8"), language="text")
else:
    st.info("No diagrams generated yet. Click **🔄 Regenerate Diagrams** above.")

